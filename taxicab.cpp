bool is_perfect_square(const fmpz_t n, fmpz_t root) {
    static fmpz_t square;
    if (fmpz_sgn(n) < 0) return false;
    fmpz_root(root, n, 2);
    fmpz_mul(square, root, root);
    return fmpz_equal(square, n);
}

